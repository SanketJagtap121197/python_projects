num = 1
while num <= 10:
    if num == 5:
        break  # Exit loop when num is 5
    print(num)
    num += 1

# Output:
# 1
# 2
# 3
# 4
#
# The break statement is used to exit the loop when num is 5.
# The loop will continue to run until num is 5, at which point the break statement will exit the loop.
# The output will be 1, 2, 3, 4.
# The break statement is used to exit the loop when a certain condition is met.
# The loop will continue to run until the condition is met, at which point the break statement will exit the loop.
