num = 1
while num < 4:
    print(num)
    num += 1
else:
    print("Loop finished")

# Output:
# 1
# 2
# 3
# Loop finished
# The else statement is used to run a block of code after the loop has finished running.
# The else statement will only run if the loop completes successfully without any break statements.
# The output will be 1, 2, 3, Loop finished.
