for i in range(3):
    for j in range(2):
        print(f"i={i}, j={j}")

# Output:
# i=0, j=0
# i=0, j=1
# i=1, j=0
# i=1, j=1
# i=2, j=0
# i=2, j=1
# The nested loop will run until the outer loop completes.
# The inner loop will run for each iteration of the outer loop.
# The output will be i=0, j=0, i=0, j=1, i=1, j=0, i=1, j=1, i=2, j=0, i=2,
